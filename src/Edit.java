    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        if(txtLength.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Missing length.");
        }else if(Integer.parseInt(txtLength.getText())>30){
            JOptionPane.showMessageDialog(this, "Max length is 30.");
        }else if(Integer.parseInt(txtLength.getText()) < 5){
            JOptionPane.showMessageDialog(this, "Minumum length is 5.");
        }else{
            PasswordGenerator pass = new PasswordGenerator();
            String password = pass.generatePassword(Integer.parseInt(txtLength.getText()));
            txtPassword.setText(password);
        }
    }//GEN-LAST:event_btnGenerateActionPerformed

