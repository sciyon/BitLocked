    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if(txtAccountName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Missing Account Name");
        }else if(txtUsername.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Missing Username");
        }else if(txtPassword.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Missing Password");
        }else if(txtEmail.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Missing Email");
        }else if(validateEmail(txtEmail.getText()) == false){
            JOptionPane.showMessageDialog(this, "Invalid Email");
        }else{
            String accname,usernm,passw,emaill;
            accname=txtAccountName.getText();
            usernm=txtUsername.getText();
            passw=txtPassword.getText();
            emaill=txtEmail.getText();
            try {
                connect();
                query ="UPDATE credentials SET cred_account_name = ?, cred_username = ?, cred_password = ?, cred_email = ? WHERE cred_id = ?";
                pst = conn.prepareStatement(query);
                pst.setString(1, accname);
                pst.setString(2, usernm);
                pst.setString(3, passw);
                pst.setString(4, emaill);
                pst.setInt(5, credId);
                pst.executeUpdate();
                disconnect();
                JOptionPane.showMessageDialog(this,"Credential successfully updated.");
            } catch (SQLException ex) {
                Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    public static boolean validateEmail(String email) {
        String regexPattern = "^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$";
        Pattern emailPat = Pattern.compile(regexPattern,Pattern.CASE_INSENSITIVE);
        Matcher matcher = emailPat.matcher(email);
        return matcher.find();
    }
