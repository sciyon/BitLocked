    public void getDetails(int credId){
        try {
            connect();
            query = "SELECT * FROM credentials WHERE cred_id = ?";
            pst = conn.prepareStatement(query);
            pst.setInt(1,credId);
            
            rs = pst.executeQuery();
            
            if (rs.next()) {
                Caccountname = rs.getString("cred_account_name");
                Cusername = rs.getString("cred_username");
                Cpassword = rs.getString("cred_password");
                Cemail = rs.getString("cred_email");
            }
            txtAccountName.setText(Caccountname);
            txtUsername.setText(Cusername);
            txtPassword .setText(Cpassword);
            txtEmail.setText(Cemail);
        } catch (SQLException ex) {
                Logger.getLogger(Create.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
