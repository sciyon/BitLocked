    public int findID(String user, String pass){
        int retval = 1;
        try {
            pst= conn.prepareStatement("SELECT * FROM users WHERE user_username = '"+user+"' AND user_pass = '"+pass+"';");
            rs = pst.executeQuery();
            
            while(rs.next()){
                String uname;
                uname= rs.getString("user_username");
                String password;
                password = rs.getString("user_pass");
                
                if((user.equals(uname))&&(pass.equals(password))){
                    retval = Integer.parseInt(rs.getString("user_id"));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        return retval;
    }
