    public void register(){
        String user = txtRegisUsername.getText();
        String pass = new String(txtRegisPassword.getPassword());
        String mail = txtRegisEmail.getText();
        
        if(verifyUsername(user) == false){
            JOptionPane.showMessageDialog(this, "Username already used.");
        }else if(verifyEmail(mail) == false){
            JOptionPane.showMessageDialog(this, "Email already used.");
        }else{
            try {
                connect();
                pst=conn.prepareStatement("INSERT INTO users (user_username, user_pass, user_email)VALUES(?,?,?)");
                pst.setString(1,user);
                pst.setString(2,pass);
                pst.setString(3,mail);
                /*
                    cant change password/email
                    users can know how much creds overall
                */
                int k = pst.executeUpdate();
                if(k==1){
                    JOptionPane.showMessageDialog(this,"Credential added");
                    
                    txtRegisUsername.setText("");
                    txtRegisPassword.setText("");
                    txtRegisEmail.setText("");
                    txtUsername.requestFocus();
                }
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
